#include <unistd.h>
#include "libft.h"

void	ft_putchar_hex(char c)
{
	char	tmp[2];

	tmp[1] = HEX[(int)c & 15];
	tmp[0] = HEX[(int)((c & 240) >> 4)];
	write(1, tmp, 2);
}
