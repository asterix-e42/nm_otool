#include <unistd.h>

void	ft_putendl_alnum(const char *s)
{
	int i;

	if (!s)
		return ;
	i = -1;
	while (*(s + ++i) && *(s + i) != '\n')
	{
		write(1, s + i, 1);
	}
	write(1, "\n", 1);
}
